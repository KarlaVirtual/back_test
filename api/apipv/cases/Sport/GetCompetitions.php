/**
 * Sport/GetCompetitions
 *
 * Obtener competiciones deportivas entre fechas específicas o por región y deporte.
 *
 * @param $_REQUEST["BeginDate"] string Fecha de inicio en formato YYYY-MM-DD.
 * @param $_REQUEST["EndDate"] string Fecha de fin en formato YYYY-MM-DD.
 * @param $_REQUEST["sportId"] int ID del deporte para el cual se obtendrán las competiciones.
 * @param $_REQUEST["regionId"] int ID de la región para filtrar las competiciones (opcional).
 *
 * @return array {
 *     "HasError": boolean Indica si ocurrió un error.
 *     "AlertType": string Tipo de alerta (e.g., "success").
 *     "AlertMessage": string Mensaje de alerta.
 *     "ModelErrors": array Lista de errores del modelo.
 *     "Data": array Datos de las competiciones obtenidas.
 * }
 *
 * @throws no
 */


/* divide una URL y obtiene un conteo de elementos. */
$obj = (explode("/", current(explode("?", $URI))));
$count = oldCount($obj);
switch ($obj[$count - 2] . "/" . $obj[$count - 1]) {
    case "Sport/GetCompetitions":
        /* Recupera competiciones deportivas entre fechas específicas y devuelve una respuesta de éxito. */

        $BeginDate = $_REQUEST["BeginDate"];
        $EndDate = $_REQUEST["EndDate"];
        $sports = (new ConfigurationEnvironment())->getCompetitions($_REQUEST['sportId'], $_REQUEST['regionId'], $BeginDate, $EndDate);

        $response["HasError"] = false;
        $response["AlertType"] = "success";
        $response["AlertMessage"] = "Operation has completed successfuly";
        $response["ModelErrors"] = [];
        $response["Data"] = $sports;


        break;

    case "OddsFeed/GetCompetitions":


        /* Se construye un filtro JSON para buscar competencias según región y deporte. */
        $sportId = $_REQUEST["sportId"];
        $regionId = $_REQUEST["regionId"];

        $json = '{"rules" : [{"field" : "int_competencia.region_id", "data" : "' . $regionId . '","op":"eq"}] ,"groupOp" : "AND"}';


        $IntCompetencia = new IntCompetencia();

        /* organiza competencias y las convierte en un arreglo con ID y nombre. */
        $competencias = $IntCompetencia->getCompetenciasCustom(" int_competencia.* ", "int_competencia.competencia_id", "asc", 0, 10000, $json, true);
        $competencias = json_decode($competencias);


        $final = array();

        foreach ($competencias->data as $competencia) {

            $array = array();
            $array["Id"] = $competencia->{"int_competencia.competencia_id"};
            $array["Name"] = $competencia->{"int_competencia.nombre"};

            array_push($final, $array);

        }


        /* asigna una respuesta exitosa con datos y mensaje de alerta. */
        $response["Data"] = $final;


        $response["HasError"] = false;
        $response["AlertType"] = "success";
        $response["AlertMessage"] = "Operation has completed successfuly";

        /* Inicializa un array vacío para almacenar errores del modelo en la respuesta. */
        $response["ModelErrors"] = [];

        break;
}
