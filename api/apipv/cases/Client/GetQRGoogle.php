/**
 * Client/GetQRGoogle
 *
 * Este script obtiene el código QR de Google Authenticator para un usuario específico.
 *
 * @param no
 *
 * @return array $response
 * - HasError (boolean): Indica si ocurrió un error.
 * - AlertType (string): Tipo de alerta (e.g., "success", "danger").
 * - AlertMessage (string): Mensaje de alerta.
 * - ModelErrors (array): Lista de errores del modelo.
 * - data (string): URL del código QR.
 * - text (string): Clave secreta de Google Authenticator.
 */


/* obtiene el valor de "id" desde la solicitud HTTP actual. */
$Id = $_REQUEST["id"];

if ($Id != "" && is_numeric($Id)) {

    /* Código inicializa objetos y verifica perfil de usuario para obtener datos de mandante. */
    $Google = new GoogleAuthenticator();

    $Usuario = new Usuario($Id);

    $UsuarioPerfil = new UsuarioPerfil($Id);

    if ($UsuarioPerfil->global == "N") {

        $Mandante = new Mandante($Usuario->mandante);

        $partner = $Mandante->nombre;

    } else {
        /* asigna el valor 'Virtualsoft' a la variable $partner si no se cumplen condiciones anteriores. */

        $partner = 'Virtualsoft';
    }


    /* Verifica si falta configuración de QR y registra un error en la respuesta. */
    if ($Usuario->saltGoogle == "") {
        $response["HasError"] = true;
        $response["AlertType"] = "danger";
        $response["AlertMessage"] = 'No tiene QR Configurado';
        $response["ModelErrors"] = [];

    } else {
        /* maneja una respuesta exitosa al intentar cambiar un estado de retiro. */

        $response["HasError"] = false;
        $response["AlertType"] = "success";
        $response["AlertMessage"] = 'No puedes cambiar el estado de un retiro ya procesado.';
        $response["ModelErrors"] = [];
        $response["data"] = $Google->getQRCodeGoogleUrl($partner, $Usuario->saltGoogle);
        $response["text"] = $Usuario->saltGoogle;

    }


} else {
    /* Manejo de errores: se establece respuesta de error con mensaje de alerta. */

    $response["HasError"] = true;
    $response["AlertType"] = "danger";
    $response["AlertMessage"] = 'Error en solicitud';
    $response["ModelErrors"] = [];

}