/**
 * Product/GetProductList
 *
 * Obtención de productos personalizados
 *
 * Este recurso permite obtener una lista de productos junto con información de sus proveedores, aplicando filtros y ordenación según los parámetros definidos.
 *
 * @param int $SkeepRows : Número de filas a omitir en la consulta.
 * @param int $MaxRows : Número máximo de filas a recuperar.
 * @param string $json : Cadena JSON con las reglas de filtrado a aplicar en la consulta.
 *
 * @returns object El objeto $response es un array con los siguientes atributos:
 *  - *Id* (int): Identificador único del producto.
 *  - *CategoryId* (int): Identificador de la categoría (actualmente siempre es 0).
 *  - *ProviderId* (int): Identificador del proveedor del producto.
 *  - *ProviderName* (string): Nombre del proveedor del producto.
 *  - *Notes* (string): Descripción del producto.
 *
 * Objeto en caso de error:
 *
 * $response["HasError"] = true;
 * $response["AlertType"] = "error";
 * $response["AlertMessage"] = "[Mensaje de error]";
 * $response["ModelErrors"] = [];
 *
 * @throws no No contiene manejo de excepciones.
 * @access public
 * @see no
 * @since no
 * @deprecated no
 */


/* Creación de un objeto Producto y configuración de parámetros para manejar datos JSON. */
$Producto = new Producto();

$SkeepRows = 0;
$MaxRows = 1000000;

$json = '{"rules" : [{"field" : "a.", "data": "1","op":"eq"}] ,"groupOp" : "AND"}';


/* Selecciona y organiza productos y proveedores en un array estructurado desde un JSON. */
$productos = $Producto->getProductosCustom(" producto.*,proveedor.* ", "producto.proveedor_id", "asc", $SkeepRows, $MaxRows, $json, false);

$productos = json_decode($productos);

$final = [];

foreach ($productos->data as $key => $value) {

    $array = [];

    $array["Id"] = $value->{"producto.producto_id"};
    $array["CategoryId"] = 0;
    $array["ProviderId"] = $value->{"producto.proveedor_id"};
    $array["ProviderName"] = $value->{"proveedor.descripcion"};


    $array["Notes"] = $value->{"producto.descripcion"};
    array_push($final, $array);

}


/* Asigna el valor de $final a la variable $response en PHP. */
$response = $final;

