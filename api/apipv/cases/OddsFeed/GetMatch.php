/**
 * OddsFeed/GetMatch
 *
 * Este script obtiene la información de un partido específico.
 *
 * @param array $params Parámetros de entrada que incluyen:
 * @param string $params->matchId Identificador del partido para filtrar la información.
 *
 * @return array $response Respuesta estructurada que contiene:
 * - bool $HasError: Indica si ocurrió un error.
 * - string $AlertType: Tipo de alerta (e.g., "success").
 * - string $AlertMessage: Mensaje descriptivo de la operación.
 * - array $ModelErrors: Lista de errores de modelo (vacío si no hay errores).
 * - array $Data: Datos del partido, incluyendo información de deporte, región y competencia.
 */


/* maneja una solicitud con un identificador de partido y genera una respuesta exitosa. */
$matchId = $_REQUEST["matchId"];

$response["HasError"] = false;
$response["AlertType"] = "success";
$response["AlertMessage"] = "Operation has completed successfuly";
$response["ModelErrors"] = [];

/* Inicializa un arreglo vacío llamado "Data" en la variable de respuesta. */
$response["Data"] = [];

if ($matchId != "") {


    /* Crea un JSON para filtrar eventos y recupera datos relacionados. */
    $json = '{"rules" : [{"field" : "int_evento.evento_id", "data" : "' . $matchId . '","op":"eq"}] ,"groupOp" : "AND"}';


    $IntEvento = new IntEvento();
    $eventos = $IntEvento->getEventosCustom(" int_evento.*,int_region.*,int_deporte.*,int_competencia.* ", "int_evento.evento_id", "asc", 0, 10000, $json, true);
    $eventos = json_decode($eventos);


    /* Extrae datos de eventos deportivos y los organiza en un array final. */
    $final = array();

    foreach ($eventos->data as $evento) {

        $array = array();
        $array["SportName"] = $evento->{"int_deporte.nombre"};
        $array["RegionName"] = $evento->{"int_region.nombre"};
        $array["CompetitionName"] = $evento->{"int_competencia.nombre"};
        $array["Name"] = $evento->{"int_evento.nombre"};

        $array["LocalStartTime"] = $evento->{"int_evento.fecha"};

        array_push($final, $array);

    }


    /* asigna un array "Objects" al elemento "Data" de la respuesta. */
    $response["Data"] = array(
        "Objects" => $final
    );


}