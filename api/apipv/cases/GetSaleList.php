 *  - *ModelErrors* (array): En caso de error, contiene los errores del modelo.
 *  - *Data* (array): Contiene los datos estructurados de los productos, con detalles sobre proveedor, mandante, estado, etc.
 *
 *
 * Ejemplo de respuesta en caso de error:
 *
