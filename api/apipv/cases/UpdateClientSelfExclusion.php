/**
 * UpdateClientSelfExclusion
 *
 * Esta función se encarga de actualizar la exclusión self del cliente. Utiliza
 * el parámetro 'ExcludeTypeId' para determinar el tipo de exclusión y
 * ajusta el valor de 'tipo' en consecuencia.
 *
 * @param object $params Objeto que contiene los parámetros necesarios
 *                       para actualizar la exclusión del cliente,
 *                       incluyendo 'ExcludeTypeId', 'ExcludedLocalDate' y 'Id'.
 *
 * @returns no
 *
 * @throws Exepcion Maneja excepciones, guarda configuración de usuario y realiza una transacción en MySQL
 *
 * @access public
 * @see no
 * @since no
 * @deprecated no
 */


/* asigna valores de parámetros a variables para su posterior uso. */
$ExcludeTypeId = $params->ExcludeTypeId;
$ExcludedLocalDate = $params->ExcludedLocalDate;
$ClientId = $params->Id;


$tipo = "";


/* asigna un valor a $tipo según el valor de $ExcludeTypeId. */
switch ($ExcludeTypeId) {
    case 6:
        $tipo = "EXCTIMEOUT";

        break;

    case 2:
        $tipo = "EXCTOTAL";

        break;

}

if ($tipo != "") {

    /* Actualiza la configuración de usuario en la base de datos y confirma transacción. */
    $valor = $ExcludedLocalDate;


    try {
        $UsuarioConfiguracion = new UsuarioConfiguracion($ClientId, $tipo);

        $UsuarioConfiguracion->setValor($valor);

        $UsuarioConfiguracionMySqlDAO = new UsuarioConfiguracionMySqlDAO();
        $UsuarioConfiguracionMySqlDAO->update($UsuarioConfiguracion);
        $UsuarioConfiguracionMySqlDAO->getTransaction()->commit();

    } catch (Exception $e) {
        /* Maneja excepciones, guarda configuración de usuario y realiza una transacción en MySQL. */

        if ($e->getCode() == 30) {

            $UsuarioConfiguracion2 = new UsuarioConfiguracion();
            $UsuarioConfiguracion2->setUsuarioId($ClientId);
            $UsuarioConfiguracion2->setTipo($tipo);
            $UsuarioConfiguracion2->setValor($valor);
            $UsuarioConfiguracion2->setUsucreaId("0");
            $UsuarioConfiguracion2->setUsumodifId("0");

            $UsuarioConfiguracionMySqlDAO = new UsuarioConfiguracionMySqlDAO();
            $UsuarioConfiguracionMySqlDAO->insert($UsuarioConfiguracion2);
            $UsuarioConfiguracionMySqlDAO->getTransaction()->commit();

        } else {
            throw new Exception($e->getMessage(), $e->getCode());
        }
    }
}

