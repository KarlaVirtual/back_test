    /**
     * Crear y devolver un objeto del tipo UsuarioNotas
     * con los valores de una consulta sql
     * 
     *
     * @param Arreglo $row arreglo asociativo
     *
     * @return Objeto $producto UsuarioNotas
     *
     * @access protected
     *
     */
    protected function readRow($row)
    {
        $producto = new UsuarioNotas();

        $producto->usunotaId = $row['usunota_id'];
        $producto->usuarioId = $row['descripcion'];
        $producto->nota = $row['nota'];
        $producto->usucreaId = $row['usucrea_id'];
        $producto->usumodifId = $row['usumodif_id'];

        return $producto;
    }


    /**
     * Ejecutar una consulta sql y devolver los datos
     * como un arreglo asociativo 
     * 
     *
     * @param String $sqlQuery consulta sql
     *
     * @return Array $ret arreglo indexado
     *
     * @access protected
     *
     */
    protected function getList($sqlQuery)
    {
        $tab = QueryExecutor::execute($this->transaction, $sqlQuery);
        $ret = array();
        for ($i = 0; $i < oldCount($tab); $i++) {
            $ret[$i] = $this->readRow($tab[$i]);
        }
        return $ret;
    }


    /**
     * Ejecutar una consulta sql y devolver el resultado como un arreglo
     * 
     *
     * @param String $sqlQuery consulta sql
     *
     * @return Array $ resultado de la ejecución
     *
     * @access protected
     *
     */
    protected function getRow($sqlQuery)
    {
        $tab = QueryExecutor::execute($this->transaction, $sqlQuery);
        if (oldCount($tab) == 0) {
            return null;
        }
        return $this->readRow($tab[0]);
    }

